package fundamentos.arrays;

public class Aula01 {
    static void main() {
        int[] idades = new int[3];
        idades[0] = 22;
        idades[1] = 23;
        idades[2] = 24;
        System.out.println("Endereço do meu objeto reference >> " + idades);

        System.out.println("idades[0] >> " + idades[0]);
        System.out.println("idades[1] >> " + idades[1]);
        System.out.println("idades[2] >> " + idades[2]);
    }
}
